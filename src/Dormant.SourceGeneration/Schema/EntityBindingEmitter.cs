using System.Collections.Generic;
using System.Linq;
using Dormant.SourceGeneration.Emit;
using Dormant.SourceGeneration.Ir;
using Dormant.SourceGeneration.Parsing;

namespace Dormant.SourceGeneration.Schema;

/// <summary>
/// Emits a per-entity <c>IEntityBinding&lt;TEntity&gt;</c> (registered via a <c>[ModuleInitializer]</c>):
/// read + schema metadata only (002 fork) — no-reflection materialization (delegates to the entity's
/// generated ctor), the schema-qualified SELECT-by-primary-key statement, the module schema name, and the
/// prebuilt <c>CREATE TABLE</c> DDL. Writes are authored DQL commands, not part of the binding. Database
/// names are schema-qualified + convention-resolved.
/// </summary>
internal static class EntityBindingEmitter
{
    private const string Abs = "global::Dormant.Abstractions";
    private const string Stmt = "global::Dormant.Abstractions.Querying.PreparedStatement";

    public static string Emit(string @namespace, EntityModel entity, string schema, NamingConvention convention)
    {
        var key = entity.Properties.FirstOrDefault(p => p.IsPrimary);
        var columns = entity.Properties; // value columns, declaration order
        var tableRef = new TableRef(schema, Table(entity, convention));
        var colNames = columns.Select(p => Col(p, convention)).ToList();
        var createTableSql = SqlRenderer.Render(new CreateTableStatement(
            tableRef,
            columns.Select(p => new ColumnDef(
                Col(p, convention), TypeMap.ToSqlType(p.DslType), !p.IsNullable, p.IsPrimary)).ToList()));

        var writer = new SourceWriter()
            .Line("// <auto-generated/>")
            .Line("#nullable enable")
            .Line()
            .Line($"namespace {@namespace};")
            .Line()
            .Open($"internal sealed class {entity.Name}Binding : {Abs}.Entities.IEntityBinding<{entity.Name}>")
            .Line("[global::System.Runtime.CompilerServices.ModuleInitializer]")
            .Line($"internal static void Register() => {Abs}.Entities.EntityBindings.Register<{entity.Name}>(new {entity.Name}Binding());")
            .Line()
            .Line($"public string Schema => {Quote(schema)};")
            .Line($"public string CreateTableSql => {Quote(createTableSql)};")
            .Line();

        EmitMaterialize(writer, entity);
        EmitSelectByKey(writer, entity, tableRef, colNames, key, convention);

        return writer.Close().ToString();
    }

    private static string Table(EntityModel entity, NamingConvention convention) =>
        NamingConventions.Resolve(entity.Name, entity.NameOverride, convention);

    private static string Col(PropertyModel property, NamingConvention convention) =>
        NamingConventions.Resolve(property.Name, property.NameOverride, convention);

    private static void EmitMaterialize(SourceWriter writer, EntityModel entity)
    {
        // Delegates to the entity's generated ctor; reads value columns positionally in SELECT order.
        writer
            .Open($"public {entity.Name} Materialize({Abs}.Querying.IFieldReader reader)")
            .Line($"return new {entity.Name}(reader);")
            .Close()
            .Line();
    }

    private static void EmitSelectByKey(
        SourceWriter writer,
        EntityModel entity,
        TableRef table,
        IReadOnlyList<string> colNames,
        PropertyModel? key,
        NamingConvention convention)
    {
        writer.Open("public " + Abs + ".Querying.PreparedStatement SelectByKey(object key)");
        if (key is null)
        {
            writer.Line("throw new global::System.NotSupportedException(\"Entity has no single primary key.\");");
        }
        else
        {
            var selectSql = SqlRenderer.Render(new SelectStatement(
                table,
                colNames,
                [new SqlCondition(Col(key, convention), "=", 1)],
                [],
                Limit: null,
                Offset: null));
            writer
                .Line($"return new {Stmt}(")
                .Line($"    {Quote(selectSql)},")
                .Line($"    writer => writer.Write(1, ({key.ClrType})key));");
        }

        writer.Close();
    }

    private static string Quote(string text) =>
        "\"" + text.Replace("\\", "\\\\").Replace("\"", "\\\"") + "\"";
}
