using Dormant.SourceGeneration.Emit;
using Dormant.SourceGeneration.Parsing;

namespace Dormant.SourceGeneration.Schema;

/// <summary>
/// Emits a strongly-typed <c>partial</c> entity class from an <see cref="EntityModel"/> (spec
/// FR-003/FR-046/FR-047/FR-048): value properties and <c>Link&lt;T&gt;</c>/<c>LinkSet&lt;T&gt;</c>
/// link members, placed in the computed namespace, with the C# <c>required</c> modifier on non-nullable
/// members. Output is deterministic (source order, <c>\n</c> newlines, invariant formatting).
/// </summary>
internal static class EntityEmitter
{
    private const string LinkType = "global::Dormant.Abstractions.Links.Link";
    private const string LinkSetType = "global::Dormant.Abstractions.Links.LinkSet";

    public static string Emit(string @namespace, EntityModel entity)
    {
        var writer = new SourceWriter()
            .Line("// <auto-generated/>")
            .Line("#nullable enable")
            .Line()
            .Line($"namespace {@namespace};")
            .Line()
            .Open($"public partial class {entity.Name}");

        foreach (var property in entity.Properties)
        {
            var name = Naming.ToPascalCase(property.Name);
            if (property.IsNullable)
            {
                writer.Line($"public {property.ClrType}? {name} {{ get; set; }}");
            }
            else
            {
                writer.Line($"public required {property.ClrType} {name} {{ get; set; }}");
            }
        }

        foreach (var link in entity.Links)
        {
            var name = Naming.ToPascalCase(link.Name);
            if (link.IsMulti)
            {
                writer.Line($"public {LinkSetType}<{link.TargetEntity}> {name} {{ get; set; }}");
            }
            else if (link.IsRequired)
            {
                writer.Line($"public required {LinkType}<{link.TargetEntity}> {name} {{ get; set; }}");
            }
            else
            {
                writer.Line($"public {LinkType}<{link.TargetEntity}> {name} {{ get; set; }}");
            }
        }

        return writer.Close().ToString();
    }
}
